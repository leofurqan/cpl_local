	function get_data(id) {
		$.ajax({
			type: 'POST',
			url: '<?php echo base_url("team/dashboard/get_points_data");?>',
			data: {tournament_id: id},
			datatype: 'jSON',
			success: function (data) {

				var json = JSON.parse(data);
				var html = '';
				var col = '';
				if (json.pools.length == 2) {
					col = "col-lg-6";
				} else if (json.pools.length == 3) {
					col = "col-lg-4";
				} else {
					col = "col-lg-3";
				}
				for (var i = 0; i < json.pools.length; i++) {
					html += '<div class=' + col + '>' +
							'<span class="font-weight-bold">Pool: ' + json.pools[i].pools_name + '</span>'
							+ '<table class="table table-striped table-bordered table-hover table-checkable"  id="table_tournaments">'
							+ '<thead>' +
							'<tr>' +
							'<th>#</th>' +
							'<th>Teams</th>' +
							'<th>P</th>' +
							'<th>W</th>' +
							'<th>L</th>' +
							'<th>T</th>' +
							'<th>Pts</th>' +
							'<th>NNR</th>' +
							'</tr>' +
							'</thead>' +
							'<tbody>';
					for (var j = 0; j < json.teams.length; j++) {
						var count = 1;
						if (json.pools[i].id == json.teams[j].pool_id) {
							for (var k = 0; k < json.points.length; k++) {
								if (json.points[k].team == json.teams[j].id) {
									var point_cal = '';
									if (json.points[k].num_wins != '0' && json.points[k].num_ties != '0') {
										point_cal = json.points[k].num_wins * 2;
										point_cal += json.points[k].num_ties * 1;
									} else if (json.points[k].num_wins == '0' && json.points[k].num_ties != '0') {
										point_cal = json.points[k].num_ties * 1;
									} else if (json.points[k].num_wins != '0' && json.points[k].num_ties == '0') {
										point_cal = json.points[k].num_wins * 2;
									} else {
										point_cal = '0';
									}
									html += '<tr>' +
											'<td>' + parseInt(j + 1) + '</td>' +
											'<td>' + json.teams[j].company_name + '</td>' +
											'<td>' + json.points[k].num_total + '</td>' +
											'<td>' + json.points[k].num_wins + '</td>' +
											'<td>' + json.points[k].num_losses + '</td>' +
											'<td>' + json.points[k].num_ties + '</td>' +
											'<td>' + point_cal + '</td>' +
											'<td>0</td>' +
											'</tr>';
								}
							}
						}
					}
					html += '</tbody>' +
							'</table>'
							+ '</div>';
				}
				$('#point_table').empty()
				$('#point_table').html(html);

			}
		});
	}

	function show(id){
		document.getElementById(id).style.display = "block";
		setInterval(function () {
			$.ajax({
				type: 'POST',
				url: '<?php echo base_url("team/dashboard/get_data_match");?>',
				data: {'match_id': id},
				datatype: 'JSON',
				success: function (data) {
					var json = JSON.parse(data);
					if (json.match_data.innings_no === "2") {
						$('#'+json.match_data.bowling_team).find('.indicator').children('span').removeClass('btatting_indicator');
						$('#'+json.match_data.bowling_team).find('.score_info').remove();
						$('#ball_score_left').removeClass('d-none');
						$('.rr').removeClass('d-none');
						let balls_left = 120 - parseInt(json.valid_balls);
						let score_left = parseInt(json.bowling_team_score) - parseInt(json.runs);
						let rr = parseFloat((score_left / balls_left) * 6).toFixed(2) ;
						$('.rr').children('h5').text(rr);

						$('#ball_score_left').html('<div class="col-sm-3"><p>Score left</p></div>' +
								'<div class="col-sm-3"><h5>'+score_left+'</h5></div>' +
								'<div class="col-sm-3"><p>Balls left</p></div>' +
								'<div class="col-sm-3"><h5>'+ balls_left+'</h5></div>'
						);
					}
					$('#'+json.match_data.batting_team).find('.indicator').children('span').addClass("btatting_indicator");
					$('#'+json.match_data.batting_team).find('.score_info').html('<div class="row"><div class="col-lg-8"><p>('+json.batting_team_balls+'/20 ov)</p></div><div class="col-lg-4"><h5 id="socre_wickets">'+json.runs+'/'+json.wickets+'</h5></div></div>')


				}
			});
		},1000)
	}
